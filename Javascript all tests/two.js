//while文
/*
繰り返し処理を行うためにはwhile文というものを用います。
whileとは「～の間」という意味の英語です。
while文は下の図のように書き、「条件式がtrueの間、{ }内の処理を繰り返す」ことができます。 {}の後にセミコロンは不要です。
*/
/*
let number = 1;
while (number <= 100) {
  console.log(number);
  number += 1;
}
*/

